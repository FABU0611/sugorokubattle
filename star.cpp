//*****************************************************************************


//*****************************************************************************
// グローバル変数
//*****************************************************************************
static int			g_TexNo = 0;		//テクスチャ番号
static int			g_SoundNo = 0;		//サウンド番号
static int			g_frame = 0;		//フレーム

//=============================================================================
// 初期化処理
//=============================================================================
void InitStar(void)
{
	//テクスチャの読み込み
	g_TexNo = LoadTexture((char*)"data/TEXTURE/star.png");
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitStar(void)
{
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateStar(void)
{
	//フレームを増やす
	g_frame++;
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawStar(void)
{
	//UV値を変えて流れているように見せる
	DrawSprite(g_TexNo, SCREEN_CENTER_X, SCREEN_CENTER_Y,
		SCREEN_WIDTH, SCREEN_HEIGHT,
		0.0f + (g_frame * 0.0025f), 0.0f - (g_frame * 0.0005f),
		1.0f, 1.0f);
}
